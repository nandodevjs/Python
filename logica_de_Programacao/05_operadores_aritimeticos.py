'''
+ = soma

- = Subtração

* = Mutiplicação

/ = Divisão

// = Divisão inteira

% = Módulo (resto da divisão)

'''

print('Soma:', 10 + 10)
print('Subtração:', 10 - 10)
print('Mutiplicação:', 10 * 10)
print('Potenciação:', 10 ** 10)
print('Divisão:', 10 / 10)
print('Divisão inteira:', 10 // 10)
print('Módulo/Resto:', 10 % 10)